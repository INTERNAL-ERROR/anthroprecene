<h1><div>The Anthropocene Reviewed</div><h4>By John Green</h4></h1>
<div class = "a">
<p class = "f">Written during 2020, John Green shares numerous essays on his life, common human experiences, and the pandemic in <strong>The Anthropocene Review</strong>. (The Anthropocene is the current geologic era we live in) After going in depth to the history, pros and cons, and his personal opinion, he ends each of his essays with a 1 to 5 star rating. Filled with detailed writing and setences that make you read it again, this is a fantastic nonfiction book. Rating and reviewing things from Diet Dr. Pepper and Scratch n' Sniff stickers to Plague and Humanity's Temporal Range, he mixes serious with lighthearted fun, throwing in anecdotes or other stories.</p></div>

<blockquote>After going through dozens of slogans through the decades - Dr. Pepper billed itself as "tasting like liquid sunshine," as the "Pepper picker-upper", as the "most original soft drink ever", - these days the company's slogan is more to the point. They call it "the on you crave". I give Diet Dr. Pepper four stars.</blockquote><cite>By John Green</cite>


